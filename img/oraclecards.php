ここからは、少し個人的な話になります。<br>
私が「オラクルカード」と出合ったのは、２００２年の春。<br><br>
その頃、私は、自分の時間もとれないほど、忙しかった仕事をやめ、これから<br>
自分のしたいことをしていこうと決め、その最初の第一歩として「ことば探し」を<br>
立ちあげる準備をしていました。<br>

しかし、自分のしたいことをしていこうと思いつつ、なかなか思ったように進めず<br>
迷いもあり、さらに、この頃は家庭的な問題も抱えておりました。<br><br>
そんなとき、私が通っていたリラックスサロン<A href=http://selfeel.ld.infoseek.co.jp/ target=_blank ><strong><u>「セルフィール」</A></strong></u>の施術師の藤永さんが、<br>スピリチュアルカウンセラーのＭさんを紹介してくれたのです。<br>
この頃はまだスピリチュアルカウンセラーは、その存在が認められているとは<br>
いえない状況でしたが、スピリチュアルカウンセラーとは、どういう方なのか<br>
興味もあったし、過去性なども聞いてみたかったし、以前から、心理学も勉強しており、<br>カウンセリングも受けていたこともあり抵抗もなく、このスピリチュアルカウンセリングを<br>
受けてみることにしたのです。<br><br>

そして、２００２年のある日予約を入れ、スピリチュアルカウンセラーのＭさんの<br>
ところに行ったのです。<br>
Ｍさんは、思っていたより若く、きれいで明るい感じながらも、どっしりと落ち着いた<br>感じの女性で、私のオーラや過去性、そして方向性などもじっくりと見てくれました。<br>
