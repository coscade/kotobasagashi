　◆【男性】リラックスするためにセックス<br>
　◆【女性】セックスの前にまずはリラックス<br><br><br>



読んでいただき、少しでも実践していただけたら嬉しいです。<br>
私もいくつか実践しています…と言いましても、<br>
まだまだ実践が足りず、研究不足の私より、<br>
専門家の話の方が説得力があると思いますし、<br>
事例も豊富なので、今回は、代表的な本から、<br>
具体的な方法をさぐっていきますね。
<br><br>
最初は、アメリカのグレイ博士の書いた本から、<br>
次は、日本人の著者の本から。<br><br>

今回も特定の方との関係を前提に書いていますので、<br>
ご了承くださいませ。
<br><br><br>


◆男性も人間的なつながりを求めている<br>
………………………………………………<br><br>

まずは、こちらの本からの紹介です。<br><br><strong>

「ジョン・グレイ博士の愛が深まる本」<br>

　　副題： 「ほんとうの歓び」を知るために <br>
　　著者： ジョン グレイ<br>  
　　★<A href=http://www.kotobasagashi.net/book/view.php?sid=2219 target=_blank ><strong><u>「「ほんとうの歓び」を知るために」</A></strong></u><br><br><br></strong>


なぜ、このグレイ博士の本を紹介するかというと、<br>
セックスを研究し尽くされた方で、多くの本や研究にも、<br>
大きな影響を与えているからです。<br>
基本的な違いや、脳の研究でわかったことなども<br>
加味して書かれていますし。<br><br>

まずは、簡単にグレイ博士のことを説明すると、<br><br>


ところが、<br>
禁欲を守る修道士生活を９年間送った２７歳のときに、<br>
ある出来事があり、一般世界に戻ります。<br>
その時、禁欲生活から解放され、断食に飢えた人間のように<br>
明けても暮れてもセックスで頭がいっぱいだったそうです。<br><br>

ここからが違います。<br>
セックスに狂った自分を見て、セックスとは何かと考え、<br>
セックスに関する書物を読みあさり、性について学び、<br>
男女の違いを研究し、人間心理を勉強し、<br>
ついに、心理学の分野で博士号を取ったのです。<br><br>

そして、さらに研究し始めます。<br>
ガールフレンドに、どこをどうすればいいのかを教えてもらい、<br>
さまざまなセックス（体位）を試み、研究会を開き、男女にとって、<br>
どんなセックスがいいかもを徹底的に研究したのです。<br>
そして、今は結婚、男女の人間関係研究者として、<br>
アメリカで大人気のカウンセラーとなっています。<br><br>

そうそう、この本には、<br>
セックス技術についても、さらりと、分かりやすく、<br>
読みやすく書いてますので、興味のある方は、<br>
参考になさってみてくださいね。<br>
もちろん、日本とアメリカの違いはあると思いますが、<br>
勉強になることは確かです。<br><br><br>



さて、この本に、こんなことが書かれています。<br>
女性が男性のセックスに疑問を持つときの事例です。<br><br><br>
<strong>

「ちょっとしたいさかいが元で、一日中口もきかず<br>
　あなたを無視してきた彼が、急にセックスを求めてくると、<br>
　あなたは戸惑ってしまう。<br>
　こんなとき、あなたの目には、男は人間的なつながりなど<br>
　まったく気にしていないように見えるだろう」と。<br><br><br></strong>


でも、「これは違う」と言うのです。<br>
実は、このとき、<br><br><br>

<strong>
「男がセックスを渇望するとき、<br>
　彼はあなたの愛に飢えているのだ。<br>
　気持ちのつながりが女にはとても大切なように、<br>
　男にはセックスがとても大切なのだ。<br><br>

　そして、男性が心を開き、相手の愛を受け入れる前には、<br>
　どうしても性的な興奮とが必要になる。<br>
　精神的な充足感を、セックスの最中に手にする<br>
　セックスで自分らしくなれる<br>
　仕事ですり減った自分を取り戻す」<br><br><br></strong>


と、男性が人間的な気持ち（愛や自分の感情）を、<br>
取り戻すのに、セックスが必要で、セックスすると、<br>
精神的にリラックスできると。<br>
男性が、それを意識してるかどうかは別にして。<br><br>

男性も人間的なつながりや愛、やすらぎを求めていること、<br>
そのために、まずは、セックスが先になりがちなことを、<br>
「女性に知ってほしい、覚えておいてほしい」<br>
と語っています。<br><br><br>


女性にしたら、とても戸惑うことですし、<br>
「なによ、この人、何を考えてるのっ。<br>
　私の気持ちなんか、どうでもいいのね」<br>
と、思ってしまいがちで、受け容れるのは、<br>
なかなか難しいことですが、<br>
誰かと話をすることによって、<br>
自分の感情を取り戻せる女性と違い、男性は、<br>
感情の取り戻し方の方法が違っているということです。<br><br>

セックスを求められたら、<br>
これは、もしかしたら、<br>
「自分の感情を取り戻したいと願っている、<br>
　男性の人間的な思いかもしれない」<br>
ということも、心にとめて置いてくださいね。<br><br><br>



さて、では、どうしたら、お互いに<br>
セックスを楽しんだり、負担なくできるようになるのか、<br>
具体的な話にはいります。<br>
引き続き、グレイ博士の本からの内容紹介になります。<br><br>

