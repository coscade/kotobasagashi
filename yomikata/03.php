<img src=<?=$URL?>img/point_bo.gif alt='' width=22 height=15 border=0><span class=yomimidashi>【1.あります、あります、間違いなく】</span><br>
<br>
想いが深まる、知識が深まる、世界が広がる、自分が成長する、<br>
刺激を受ける、元気になるなどもっともっとあるかもしれませんが…。 <br>
人と話をしてもそういうことはあるでしょうが、<br>
本の場合は、わからないとき、何度も見返すことができますし、<br>
気になる所に引き戻ることもできます。 <br>
<br>
<span class=yomiorange>
つまり自分自身と会話したり、著者に問いかけしたりして、<br>
知識や想いが自分のものになり、<br>
それが幾十にも積み重なっていく</span>って感じでしょうか。 <br>
本は、後から徐々にじっくりと効いてきて、<br>
自分の内からの力になるような感じがします。<br>
知らずに自分の知識となり、静かに積もっていくのです。 <br>
<br>
話し方も、話す内容も違ってきます。 <br>
一説によると、本を読んでいる人と、読んでない人の話し方は違うとか。 <br>
読んでない人の話し方は、<span class=yomibold><br>
「文章の始まりと終わりがきっちりと呼応してない傾向が見られる」
</span>といいます。 <br>
また、本を読んでいると会話の中で<br>
「そういえば、あの本にこんなこと書いてあったな」とか、<br>
「これはあの本に書いてあった通りだな」などと、<br>
自分の見方でない少し客観的なものの見方が出来、<br>
幅のある会話ができるようになる思います。 <br>
