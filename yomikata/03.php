<br>
多少でも<span class=yomibold>知っていると、<br>
人との接し方、自分との折り合いのつけ方など、<br>
対応にかなりの違いが出る</span>と思います。<br>
<span class=yomiorange>
「人間とはどんなもの？どんな気持ちを持っているの？<br>
　こんなときにはどんな気持ちになるの？」</span><br>
などなど、深くて複雑な人間の気持ちを少し学んでみませんか？ <br>
<br>
例えば子どものこんな気持ちはどうでしょう。<br>
知っているのといないとでは、心構えが違うと思いませんか？ <br>
以下は、『「人生学」ことはじめ』という本で紹介されている一部です。 <br>
<br>
  <span class=yomibold>
『思春期の子どもたちは、いじめなどのことが明るみにでたとき、<br>
　「しまった」と思う反面、「ほっとした」という場合が多い。<br>
　心のどこかで「とめてくれ」と叫んでいる。<br>
　そして、誰もとめてくれないとなると、それはエスカレートせざるを得ない。』</span> <br>
<br>
 <span class=yomibold>
『子ども指導ですがある程度よくなったところで、<br>
　もう一度、がらっと悪くなるという例が多いんです。<br>
　その時、先生は「裏切られた」と思うわけです。<br>
　独身の先生で、“非行少年”を下宿に連れて来て、一緒に生活をした。<br>
　その子がよくなってきたと喜んでいた矢先、先生の月給を盗んで逃げたという。<br>
　先生はもうカンカンでした。「裏切った」と、ね。<br>
　これはものすごく浅はかな考えです。<br>
　そこまでやってもらったときというのは、<br>
　子どもはそういうことをする場合が多いんです。<br>
　（先生の愛情が）本物かどうか試している……。』</span><br>
こんな事例がたくさん載っていて、考えさせられます。<br>

<br>
<span class=yomipurple>深い人間理解をしたいとき→人間の心理は深いなぁと思います。</span><br>
