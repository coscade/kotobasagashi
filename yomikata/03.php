<br>
なかなか会うことができないような有名人、或いは、<br>
歴史上の人物からの問いかけに答えることもできますし、<br>
答えを見つけることもできます。 <br>
また、生きる支えになる人物と出会うこともあります。 <br><br>
かのエジソンさんも <br>
<span class=yomiorange>「本ほど時代や世界を超えて、<br>
対話を楽しめる相手はいない」</span> <br>
と言っています。<br>
過去を知りたいなら、歴史本、古典、伝記などをおすすめします。 <br>
<br>
次のことばを読んでみてください。 <br>
<br>
<span class=yomibold>  
『あなたはわきがや口臭のある人間に腹を立てているのか。<br>
　いったい彼にどうせよというのだ。<br>
　彼にしたって好きこのんでわきがや口臭を漂わせているわけではない。<br>
　「しかし分別ある人間なら、ちょっと考えただけで<br>
　何か他人の気にさわるかぐらいわかりそうなはずだ」<br>
もっともな意見だ。<br>
だが、あなただって分別があるのだから、<br>
その分別を使って相手に悟らせてみてはどうだ。<br>きちんと説明して気づかせればいいのではないか』</span><br>
<br>
これは、１２１年に生まれた、<br>
ローマ５賢帝の一人マルクス・アウレリウスのことばです。<br>その頃にも、今と同じような悩みがあったのですね（笑）<br>
もっとも、こんな深いこともちゃんと言ってます。<br>
<br>
<span class=yomibold>
「時は流れる川である。<br>
　生きとしいけるものすべてが、容赦なくその流れに運ばれていく。<br>
　今見えたとおもったものがあっという間に過ぎ去り、<br>
　また何かが運ばれてきたかと思うと、すぐに波に押し流されてしまう」</span><br>
いつの時代も…生きることは無常だったのですね… <br>
<br>
また、たとえばシャネル。<br>
シャネルは孤児院で育ち、若い頃は娼婦や、愛人だったりもしましたが、<br>
その中からはい上がって大きな成功を収めました。<br>
晩年は、一人暮らしで仕事に賭けていたと言います。<br>
とても仕事が好きで、みんなの休日である新年や日曜日が終わって<br>
「また仕事の時間がはじまること」をとても待っていたそうです。<br>
よくこんなことを言っていたとか。<br>
 <span class=yomibold>「みんな働きたくないの？恥ずかしいことだわ。
   働くのはあなたたちのためなのよ」</span> <br>
 また、こんなことも言っています。 <br>
 <span class=yomibold> 「エレガンスとは、新しいドレスを着ることではない。<br>
  人はエレガントだからエレガントなのだ。新しいドレスを着たからといって、
  それは変わるものではない。<br>
 よく選ばれた一枚にスカートと一枚のトリコを着てもエレガントになれる」<br>
  </span> 
<br>
シャネルのことをちょっとでも知れば、シャネルブランドに対して、<br>
もう少し深い思いをいだくのではないでしょうか。<br>
私はシャネルブランドは持っていませんが、<br>
このように過去の偉大な女性達の生き方や、ことばを、<br>
本を通じて学べることは生きる上でとても参考になるのではないでしょうか。<br>
<br>
<span class=yomipurple>過去の偉人たちがどう生きていたか知りたい→ひもといてみましょう。</span><br>
