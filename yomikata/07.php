    <br>
    また、いつも本を予約する時には、近所の街角書店が便利です。<br>
    顔見知りになると、融通を利かせてくれることもあります。<br>
    <br>
    個性的な専門店も増えています。<br>
    専門書を選ぶ時には、ぜひ行ってみることをおすすめします。<br>
    専門店に行き、本を選ぶ相談をしたり、選んでもらうのも楽しいですよ。<br>
