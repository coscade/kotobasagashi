    <span class=yomiorange>そして、本を探すのが苦手という人には、<br>中規模書店をおすすめします。</span><br>
    大規模書店に行くとかなりの量の本が出回っていて、くらくらしてしまいますから、<br>選び慣れていない人は、中規模書店がいいと思います。<br>
    <span class=yomibold>
ワンフロアでタイトルがざっくりと見渡せるし、<br>他の分野も気軽に見ることができます。<br>また、本を置く分量がかなり限られていて、売れ筋本を置いているので、<br>
今時の本を探すにはもってこいです。</span><br>
    どこにどの本があるのかもわかりやすくなっているので、<br>
    大規模書店でうろうろと迷うよりは、疲れないと思います。<br>
