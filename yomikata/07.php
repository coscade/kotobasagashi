            <img src=/img/point_bo.gif alt='' width=22 height=15 border="0"><span class=yomimidashi>【４．古本屋の上手な利用の仕方…街の古本屋】</span><br>
            <br>
            一見、とっつきにくくて無愛想な感じの店主がいるのが古本屋さん…<br>
            というイメージがありますが、（ご本人達も自覚しているようです（笑））…<br>
            「どう利用していいのかわからない」方が案外多いと思います。<br>
            確かに、新古書店などと利用の仕方は違いますね。<br>
            だいたいこんなふうに分けてみました。<br>
            <span class=yomibold>
◇新古書店は、新書、比較的新しい本を買うとき利用</span><br>
            　店員さんに本のことを聞いてもアルバイトなので、ほとんどわからない、<br>
            　答えが得られない。ただひたすら本棚を自分で探す。<br>
            　見たいなと思った本があったらめっけもの。<br>
            <span class=yomibold>
◇古本屋は、資料的な価値ある本、絶版された本、中身の価値ある古い本<br>
（いわゆる黒本）など、探す場合に利用<br></span>
            　専門知識が豊富なので、探し本は見つかりやすい。<br>
            　しかも探し本についての相談や、見つかりそうな場所も教えてくれる。<br>
            　古い本についての知識は豊富。<br>

            <br>
            さて、古本屋の利用の仕方ですが、実際に、<br>
            <span class=yomibold>神田神保町にある古本屋「かんたんむ」さん</span>にお話を<br>聞いてきました。<br>
            ここは、何度かお世話になっている古本屋さんです。
            <br>
            <br>
            そんな古本屋さんと上手につき合って、欲しい本を見つけるコツは、<br>
            <span class=yomibold>「その古本屋さんの得意分野を見つける、<br>
一度でも本を買い、なじみ客になり何度か通う」</span><br>
            店主さんに話しかけるコツは、<br>
            <span class=yomibold>「それとなく、さりげなく話しかける」と「多くの質問をしない」</span>ことだとか（笑）<br>
            <span class=yomibold>
質問がある時には、欲しい本、欲しい情報をこちら側である程度整理しておき、<br>
尋ねる</span>といいそうです。<br>
            <br>
            その古本屋さんの得意分野を見つけるには、今はネットがあったりしますし、<br>
            古本屋街マップなどを上手に活用するといいようです。<br>
            古本屋街マップは、その古本屋さんで聞くと手に入れることが出来ると思います。<br>
            <br>
            また、本を売るときは、いきなり持っていかずに、<br>
            「電話か店頭で相談してみてからがいい」そうです。<br>
            <span class=yomiorange>
絶版になった本、全集が揃っている本で、文庫になっていない本などは<br>
比較的高く売れる</span>そうなので、古本屋に相談してみるといいかも…
            <br>新古書店などで売るより、高く値がつくこともあります。<br>

            ぜひ、利用してみてください。<br>
