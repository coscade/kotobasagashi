        <img src=/img/point_bo.gif alt='' width=22 height=15 border="0"><span class=yomimidashi>【３．古本屋の上手な利用の仕方…新古書店】</span><br>
        （※便宜上、「ブックオフ」などの最近の大型古本屋は新古書店としています）<br>
        <br>
        新書を扱う本屋では、売れ筋中心に本が選択され、置かれていますので、そのときに<br>買わないと、いつの間にかなくなっていて欲しい時に探せない状態になります。<br>
        売れない本はどんどん、返品され処分されてしまうんですよね。<br>
        出版社としては、在庫しているだけで保管料、倉庫代、税金もかかってくるので、<br>早々と処分してしまわねばならない事情もあるらしいです。<br>
        <br>
        つまり、<br><span class=yomiorange>
２年以上前の本なら、新古書店か、古本屋か、ネットで探さないと<br>
見つからない可能性が大きい</span>ということになります。<br>
        実際、発売から４～５年以上たった本は、書店で買いたいと思っても、<br>
        探すのはかなり手間がかかります。<br>
        書店にあるパソコン（よく壊れているし）で調べるのも時間がかかりますし、<br>
        棚の中からその本を見つけるのも大変です。<br>
        よほどのベストセラーでもない限り、端っこに追いやられますし。<br>
        <span class=yomibold>
急がない本、そのうち読みたい本であれば、<br>
新古書店、古本屋を利用するといいでしょう。</span><br>
        <br>
        お手軽な新古書店「ブックオフ」は便利ですよね。<br>
        <span class=yomiorange>特に「１０５円コーナー」は利用する人も多いとか。確かにありがたいです。</span><br>
        しかし、聞くところによるとこの１０５円コーナーに置かれた著者は、<br>
        かなりショックを受けるとか…しかも、１０５円でも売れ残っているとさらに…。<br>
        そうですよね、３年前くらいに３０００円した本が、<br>
        １０５円で販売されていたりするのですから。<br>
        <br>
        しかし、さらに聞くところによる、時々<span class=yomibold>「掘り出し本」</span>があるとか。<br>
        新古書店の本の買い取りは、本の内容や価値に関係なく売り買いされる<br>
        （文庫本は１０～６０円、ハードカバーは６０～１５０円目安だとか）ので、<br>
        新しい本ほど高く、古い本は安くなってしまいます。<br>
        しかし、<span class=yomibold>古本屋にしたら、<br>
とんでもなく価値ある本（つまり、こういう本は古本屋では高く売れる）が、<br>
この１０５円棚にあったりする</span>そうです。<br>１０５円コーナーは侮れません、ぜひ、チェックを！
